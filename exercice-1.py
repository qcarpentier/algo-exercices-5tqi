# Écrire un programme qui affiche les nombres de 1 à 100 en indiquant le départ, le milieu et l'arrivée.

### Solution avec la boucle for ###

print('Zé barti!')

for number in range(1, 101):
    print(number)

    if number == 50: print('Zé la moitié!')
    if number == 100: print('Zé derminé!')
    

